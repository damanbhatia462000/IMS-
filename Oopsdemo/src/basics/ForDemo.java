package basics;
//For demo
public class ForDemo {

	public static void main(String[] args) {
		int i,j;
		
		System.out.println("***Displaying numbers from 1-25***");
		for(i=0;i<=25;i++) {
			System.out.println(i);
		}
		
		System.out.println("***Displaying numbers from 50-1 in reverse order***");
		for(j=50;j>0;j--) {
			System.out.println(j);
		}

	}

}
